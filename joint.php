            <p style="text-align:left;">(100年3月7日所務會議通過)</p>
            <ul style="list-style-type:decimal;text-align:left;">
　<li>國立臺灣大學理學院海洋研究所（以下簡稱本所）為建立適合本所的公用核心設施與研究空間、支援本所相關研究，增進空間與經費使用效率，促進本所之研究發展得成立共同實驗室，並為落實共同實驗室管理，特訂定本辦法。</li>
<li>本所共同實驗室之建置由本所專任教師向所務委員會提出申請，申請書內容須說明申請理由，實驗室規劃或是設計圖，經費分攤與管理使用規則，由所務會議決議是否建立該項共同實驗室，並經學校相關辦法辦理後設立。</li>
<li>各共同實驗室由參與教師成立管理小組並推舉召集人(負責人)，並應經所務會議確認。管理小組召集人統籌管理共同實驗室之運作，並加強環安衛監督。</li>
<li>管理小組每年應定期向所務委員會議報告該實驗室運作狀況與使用效率，績效不彰或有違原設定宗旨者由所務委員會議提請所務會議決定是否取消或是同意該實驗室繼續運作。所務會議議決時該實驗室參與人員應自行迴避。</li>
<li>本辦法如有未盡事宜，得提報所務會議討論決議之。</li>
<li>本辦法經所務會議通過後，自發布日施行。</li>
</ul>

